define('ajax-transport',['loglevel', 'url-utils', 'one-login', 'jquery-internal', 'oauth'], function (log, utils, oneLogin, jqi, OAuth) {
	

	var ONE_USER_PATH = '/one';
	var ONE_OAUTH1_PATH = '/one/oauth1';

	var INTERACTION_PATH = '/rt/api/2.0/interaction';
	var PROFILE_PATH = '/rt/api/2.0/profiles';

	return function (p) {

		var oauth;
		var oneAuthRetries = 0;

		if (p.oauth1) {

			utils.precondition(p.oauth1.apiKey, 'Missing OAuth1 credential: apiKey');
			utils.precondition(p.oauth1.sharedSecret, 'Missing OAuth1 credential: sharedSecret');
			utils.precondition(p.oauth1.loginId, 'Missing OAuth1 credential: loginId');

			// initialize the OAuth module
			// jscs:disable
			oauth = new OAuth({
				consumer: {
					public: p.oauth1.apiKey + '!' + p.oauth1.loginId,
					secret: p.oauth1.sharedSecret
				},
				signature_method: 'HMAC-SHA1'
			});
			// jscs:enable

			log.debug('ONE - Using OAuth 1.0 AJAX transport');

		} else {

			oneAuthRetries = (p.oneauth && p.oneauth.retries >= 0) ? p.oneauth.retries : 2;

			log.debug('ONE - Using ONE-Auth AJAX transport');

		}

		var config = p;

		/**
		 * Makes an AJAX request to ONE
		 *
		 * @param {Object} r - the request
		 * @returns {Object} - The AJAX request promise
		 */
		function makeRequest(r) {

			var go = function () {

				log.debug('ONE - Invoking: ', r.method, r.url);

				var deferredResult = jqi.Deferred();

				jqi.ajax(r).done(function (resp) {

					deferredResult.resolve(resp);

				}).fail(function (resp) {

					var error = resp.responseJSON || {
						statusCode: resp.status,
						statusMessage: 'ONE responded with status: ' + resp.statusText
					};

					deferredResult.reject(error);
				});

				return deferredResult.promise();
			};

			return doWithRetry(go);
		}

		/**
		 * Performs retries for api calls when the API returns 401 (unauthenticated).  When 401, then invoke the login
		 * pop-up, and on successful login then retry the specified function.  This function can be wrapped around any
		 * other functions that return promises.
		 *
		 * @param {Function} funcThatReturnsPromise - function to retry
		 * @returns {Object} jQuery promise
		 */
		function doWithRetry(funcThatReturnsPromise) {

			var deferred = jqi.Deferred();

			var retries = oneAuthRetries;

			var retry = function (wrappedFn) {

				wrappedFn().done(function (response) {

					deferred.resolve(response);

				}).fail(function (response) {

					if (response.statusCode === 401 && retries > 0) {

						log.info('ONE - User was not authenticated, attempting login, then retry...');

						retries--;

						// trigger login pop to appear
						oneLogin(config).login().done(function () {

							// on successful login, retry function call
							retry(funcThatReturnsPromise);

						});

					} else {

						// let caller handler non-auth failures
						deferred.reject(response);

					}

				});

			};

			retry(funcThatReturnsPromise);

			return deferred.promise();
		}

		/**
		 * Sets the authentication headers on the request (OAUTH 1.0)
		 *
		 * @param {Object} r - The request
		 * @returns {Object} The updated request
		 */
		function auth(r) {
			if (oauth) {
				r.url = config.host + ONE_OAUTH1_PATH + r.url;
				r.headers = jqi.extend(r.headers, oauth.toHeader(oauth.authorize({ url: r.url, method: r.method})));
			} else {
				r.url = config.host + ONE_USER_PATH + r.url;
				// Ensure IDM knows this is an AJAX request, not a user request
				r.headers = jqi.extend(r.headers, { 'X-Requested-With': 'XMLHttpRequest' });
				r.crossDomain = true;
				r.xhrFields = {
					withCredentials: true
				};
			}
			return r;
		}

		/**
		 * Gets the Optimzation points for the provided interaction
		 *
		 * @param {Object} customer - The customer data
		 * @param {String} interactionUri - The Interaction URI
		 * @param {Array<Object>} properties - The ONE-API properties
		 * @returns {Object} - The AJAX request promise
		 */
		function sendInteraction(customer, interactionUri, properties) {

			return makeRequest(auth({
				url: INTERACTION_PATH + '?' + jqi.param({
					sk: config.sk,
					pv: config.pv,
					tid: customer.tid
				}),
				method: 'POST',
				data: JSON.stringify({
					uri: interactionUri,
					customerKey: customer.customerKey,
					properties: utils.objectToNameValuePairs(properties)
				}),
				headers: {
					// force PC8 mode to get actions back as data structures
					dataMimeType: 'application/json'
				},
				contentType: 'application/json',
				dataType: 'json'}));

		}

		/**
		 * Sends a Capture or Track event to ONE
		 * @param {Object} customer - The customer that performed the interaction
		 * @param {String} interactionUri - The URI of the interaction
		 * @param {Object} properties - The ONE-API properties
		 * @returns {Object} - The AJAX request promise
		 */
		function updateInteraction(customer, interactionUri, properties) {

			return makeRequest(auth({
				url: INTERACTION_PATH + '?' + jqi.param({
					sk: config.sk,
					pv: config.pv,
					tid: customer.tid
				}),
				method: 'PUT',
				data: JSON.stringify({
					uri: interactionUri,
					customerKey: customer.customerKey,
					properties: utils.objectToNameValuePairs(properties)
				}),
				headers: {
					// force PC8 mode to get actions back as data structures
					dataMimeType: 'application/json'
				},
				contentType: 'application/json',
				dataType: 'json'}));

		}

		/**
		 * Sends a Capture or Track event to ONE
		 * @param {Object} customer - The customer that performed the interaction
		 * @param {String} interactionUri - The URI of the interaction
		 * @param {Object} properties - The ONE-API properties
		 * @returns {Object} - The AJAX request promise
		 */
		function sendProperties(customer, interactionUri, properties) {

			return updateInteraction(customer, interactionUri, properties);
		}

		/**
		 * Sends the customer's response to an Optimization (an Action Response)
		 * @param {Object} customer - The customer that responded to the action
		 * @param {String} responseCode - The response code
		 * @param {Object} properties - The ONE-API properties
		 * @returns {Object} - The AJAX request promise
		 */
		function sendResponseCode(customer, responseCode, properties) {

			properties = jqi.extend(properties, { _RESP_CODE_: responseCode });

			return updateInteraction(customer, null, properties);

		}

		/**
		 * Gets the structure data
		 *
		 * @param {Object} customer - The customer to get the Structure for
		 * @param {String} structureServiceName - The name of the Structure
		 * @param {String} format - The output format of the structure (e.g. 'application/json' or 'text/html')
		 * @returns {Object} - The AJAX request promise
		 */
		function getStructureData(customer, structureServiceName, format) {

			// even though jqi supports converstion of 'data' to params, we create the full URL here so the oauth
			// signing process will include the full URL including params.
			return makeRequest(auth({
				url: PROFILE_PATH + '/' + structureServiceName + '?' + jqi.param({
					sk: config.sk,
					pv: config.pv,
					tid: customer.tid,
					ck: customer.customerKey,
                    ckname: customer.customerKeyName
				}),
				method: 'GET',
				dataType: format}));
		}

		return {
			sendInteraction: sendInteraction,
			sendResponseCode: sendResponseCode,
			sendProperties: sendProperties,
			getStructureData: getStructureData
		};

	};

});

